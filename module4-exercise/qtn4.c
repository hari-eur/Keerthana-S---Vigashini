/*4. Write a program to Check string if it is 8 character length, contains atleast one lowercase and uppercase,one
number,one special character and possible permutations using pointers*/

#include <stdio.h>
void permute(char*,int ,int );
int main()
{
    char str[10];
    printf("Enter the string\n");
    scanf("%s",str);
    int i=0,low=0,upr=0,spl=0,num=0;
    int len=0;
    while(str[i]!='\0')
    {
        if(str[i]>=97 && str[i]<=122 )
        {
            low++;            
        }
        if(str[i]>=65 && str[i]<=90)
        {
            upr++;
        }
        if(str[i]>=48 && str[i]<=57)
        {
            num++;
        }
        if((str[i]>=33 && str[i]<=47) || (str[i]>=58 && str[i]<=64) || (str[i]>=91 && str[i]<=96) ||(str[i]>=123 && str[i]<=126)  )
        {
            spl++;
        }
        len++;
        i++;
    }
    if(len<8)
        printf("Must be of length 8");
    else if(low==0)
        printf("Must contain atleast one lowercase");
    else if(upr==0)
        printf("Must contain atleast one uppercase");
    else if(spl==0)
        printf("Must contain atleast one special case");
    else{
        printf("It is a valid string\n");
        printf("the possible permutations are ;\n");
            permute(str,0,len-1);
    }
    return 0;
}

void permute(char *str,int left,int right){
    int i,j;
    char temp;
    for(i=left;i<=right;i++){
        for(j=i+1;j<right;j++){
            temp=str[i];
            str[i]=str[j];
            str[j]=temp;
            permute(str,i+1,right);
            temp=str[i];
            str[i]=str[j];
            str[j]=temp;

        }
    }
    printf("%s\n",str);
}
